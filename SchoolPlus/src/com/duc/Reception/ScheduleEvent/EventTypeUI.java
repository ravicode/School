    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        EventType et = new EventType();
        et.typename = txtEventType.getText();
        if(et.typename.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Type Name is Empty","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        boolean b = new EventDB().addEventType(et);        
        if(b)
        {
            dm.removeAllElements();
            loadTypes();
            JOptionPane.showMessageDialog(this, "New Type Added");
        }
        else
        {
            JOptionPane.showMessageDialog(this, "New Type Not Added","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

