    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        Event ev = new Event();
        ev.eventDetail = txtDesc.getText();
        ev.eventDate = cmbDate.getText();
        ev.eventVenue = txtVenue.getText();
        ev.eventCatagory = cmbPriority.getSelectedItem().toString();
        //EventType et = (EventType)typeproperty.get(cmbType.getSelectedItem().toString());
        int j =new EventDB().getTypeId(cmbType.getSelectedItem().toString());
        ev.typeId = j;
        System.out.println(ev.typeId);
        ev.eventHost = txtHost.getText();
        ev.eventTime = txtTime.getText();
        ev.eventRemark = txtRemark.getText();
        boolean b = new EventDB().saveEvent(ev);
        if(b)
        {
            JOptionPane.showMessageDialog(this, "Event Saved");
            add.setEnabled(false);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Event Not Saved","Error",JOptionPane.ERROR_MESSAGE);
        }

}//GEN-LAST:event_addActionPerformed

