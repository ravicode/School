  private void loadTypes()
    {
        ArrayList<EventType> types = new EventDB().loadEventTypes();
        if(types==null)
        {
            return;
        }
        for(EventType et : types)
        {
            typeproperty.put(et.typeId, et);
            cmbType.addItem(et.typename);

        }
    }
    private void loadEvents()
   {   
       
        String command="";
        if(date.isSelected()||type.isSelected()||priority.isSelected())
        {
            command="  where ";
            if(date.isSelected())
        {
            String c = cmbDate.getText();
            System.out.println(c);
            command+=" event_date = '"+c+"' and ";
        }
        
        if(type.isSelected() )
        {
            
            String n = cmbType.getSelectedItem().toString();
            int j =new EventDB().getTypeId(n);
            command+="  type_id = "+j+" and ";
        }
        
        
        if(priority.isSelected())
        {
            String f = cmbPriority.getSelectedItem().toString();
            command+= " event_priority = '"+ f +"' and ";
        }
        command+=" type_id>0";
        }
        
        else
        {
             command="";
        }  
         
        
        DefaultTableModel dm = (DefaultTableModel)jTable1.getModel();
        ArrayList<Event> events = new EventDB().loadEvents(command);
        if(events==null)
        {
            dm.setRowCount(0);
            return;
        }
        dm.setRowCount(events.size());
        Event ev = null;
        EventType et=new EventType();
        for(int i=0;i<events.size();i++)
        {
            ev = events.get(i);
            //EventType et =(EventType)typeproperty.get(ev.typeId);
            et.typename=new EventDB().getType(ev.typeId);
            System.out.println(et.typename);
            dm.setValueAt(et.typename, i, 0);
            dm.setValueAt(ev.eventDate, i, 1);
            dm.setValueAt(ev.eventTime, i, 2);
            dm.setValueAt(ev.eventCatagory, i, 3);
            dm.setValueAt(ev.eventVenue, i, 4);
            dm.setValueAt(ev.eventHost, i, 5);
            dm.setValueAt(ev.eventDetail, i, 6);
        }
    }
