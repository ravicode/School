public void saveHoliday()
{
     //con= new com.duc.DatabaseConnection.DatabaseConnection().dataConnection();

            try
            {
                ps=con.prepareStatement("select * from holiday where Date=?");
                ps.setString(1,cmbDate.getText());
                rs=ps.executeQuery();
                if(rs.next())
                {
                 JOptionPane.showConfirmDialog(null,"Holiday is already set","Holiday Set",JOptionPane.PLAIN_MESSAGE);
                }else
                {
               ps=con.prepareStatement("insert into holiday values(?,?,?)");


                 ps.setString(1,Holidaytitle.getText());//remarks

                 ps.setString(2,cmbDate.getText());

ps.setInt(3,sessionValue);
                ps.executeUpdate();

           ///con.close();
            }
            }
            catch(Exception e)
            {
e.printStackTrace();
            }
     showAllHoliday();
}

