public void sessionWiseHoliday()
{
    try
       {
           ps = con.prepareStatement("select holidaytitle,Date,session,sessiontitle,sessionid from holiday left join sessionsetup on session=sessionid where sessiontitle=?");
ps.setString(1,sessionCombo.getSelectedItem().toString());
           rs = ps.executeQuery();
            int i = 0,j=0;
            if(rs.next())
            {
                rs.first();
                rs.previous();
          while(rs.next())
            {
                j++;
            }
            rs.first();
            rs.previous();
            dm.setRowCount(j);
            while(rs.next())
            {
                dm.setValueAt(rs.getString(1), i, 0);
               dm.setValueAt(rs.getString(2), i, 1);

                i++;
            }
           // con.close();
            System.out.println("Database closed");

       }else
           dm.setRowCount(0);
    }
       catch(Exception e)
       {
           System.err.println("Error"+e);
       }
}
    public void fromSession()
    {
         try
       {
           ps = con.prepareStatement("select sessiontitle,sessionid,status from sessionsetup ");

           rs = ps.executeQuery();
           // int i = 0,j=0;
          while(rs.next())
            {
                //j++;
              sessionCombo.addItem(rs.getString(1));

              if(rs.getInt(3)==1)
                  sessionValue=rs.getInt(2);

            }



         }
         catch(Exception e)
         {
             e.printStackTrace();
    }



    }
