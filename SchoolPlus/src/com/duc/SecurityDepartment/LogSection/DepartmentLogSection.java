    public void populateTable(ResultSet rs)
    {
        int i = 0,j=0;
       // con=new com.duc.DatabaseConnection.DatabaseConnection().dataConnection();
        try
        {
        while(rs.next())

            {
                j++;
            }

            rs.first();
            rs.previous();
            dm.setRowCount(j);
            while(rs.next())
            {
                ps=con.prepareStatement("select * from logdepartmentsetup where departmentid=?");
                ps.setString(1,rs.getString(1));
                rs1=ps.executeQuery();
if(rs1.next())
{
                dm.setValueAt(jComboBox1.getSelectedItem().toString(), i, 0);//username
                dm.setValueAt(rs.getString(2), i, 1);//ipaddress
                dm.setValueAt(rs1.getString(2), i, 2);//departmentname
                dm.setValueAt(rs.getString(5), i, 3);//passwordofdepartment
                 dm.setValueAt(rs.getString(3), i, 4);//date
                dm.setValueAt(rs.getString(4), i, 5);//time


}

                i++;

            }
          //  con.close();
        }catch(Exception e)
        {
e.printStackTrace();
        }
            System.out.println("database closed");
    }

