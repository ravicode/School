    public void populate(ResultSet rs)
    {
        String user=null;
        int i=0,j=0;
        try
        {
             while(rs.next())

            {
                j++;
            }

            rs.first();
            rs.previous();
            dm.setRowCount(j);
            while(rs.next())
            {
                user=rs.getString(1).trim();
             ps = con.prepareStatement("select decode(?,'bluegene') from useraccounts");
             ps.setString(1, user);
               rs1=ps.executeQuery();
rs1.next();
                dm.setValueAt(rs1.getString(1), i, 0);//username
                dm.setValueAt(rs.getString(2), i, 1);//password
                dm.setValueAt(rs.getString(3), i, 2);//date
                dm.setValueAt(rs.getString(4), i, 3);//time
dm.setValueAt(rs.getString(5), i, 4);//ip address
                i++;

            }

              ps = con.prepareStatement("select decode(username,'bluegene') from useraccounts");


        }catch(Exception e)
        {
e.printStackTrace();
        }

    }
