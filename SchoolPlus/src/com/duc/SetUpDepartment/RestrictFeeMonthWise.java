    public void removeMonthFee()
    {
         int row = jTable1.getSelectedRow();
         int choose=0;
         System.out.println("row no. selected"+row);
        try
        {
           ps=con.prepareStatement("select * from feedues where month=? and head=? and feetype=0");
          ps.setInt(1,Integer.parseInt(month1.get(jComboBox1.getSelectedItem().toString()).toString()));
        ps.setInt(2,Integer.parseInt(monthlyfeemap1.get(dm.getValueAt(row, 0).toString()).toString()));

          rs=ps.executeQuery();
         if(rs.next())
         {
             choose=JOptionPane.showConfirmDialog(null,"Fee Present in this Dues,still want to delete?","Fee Present",JOptionPane.OK_OPTION);
         }
         if(choose==JOptionPane.OK_OPTION)
         {
         ps=con.prepareStatement("delete from restrictfeemonthwise where month=? and feeid=?");

         ps.setInt(1,Integer.parseInt(month1.get(jComboBox1.getSelectedItem().toString()).toString()));
        ps.setInt(2,Integer.parseInt(monthlyfeemap1.get(dm.getValueAt(row, 0).toString()).toString()));
ps.executeUpdate();
         dm.removeRow(row);
         // con.close();
         System.out.println("DataBase Conection Closed");
    }
        }
        catch(Exception e)
        {
            System.err.println("Error"+e);
        }
    }

