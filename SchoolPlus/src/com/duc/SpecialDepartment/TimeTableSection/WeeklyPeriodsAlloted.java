    public void loadList()
    {
        try
        {
         ps=con.prepareStatement("select teachercode,name,count(*) from periodallotment, staffentry where teachercode=code group by teachercode");
         rs=ps.executeQuery();
         int i=0;
         while(rs.next())
         {
         i++;
         }
         model.setRowCount(i);
         rs.first();
         rs.previous();
         i=0;

         while(rs.next())
         {
         model.setValueAt(rs.getString(1), i, 0);
         model.setValueAt(rs.getString(2), i, 1);
         model.setValueAt(rs.getString(3), i, 2);
         i++;
         }
        }
        catch(Exception e)
        {
         e.printStackTrace();
        }
    }

