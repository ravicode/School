    public void search (String command)
    {
     try
     {
    // con= new com.duc.DatabaseConnection.DatabaseConnection().dataConnection();
     ps=con.prepareStatement("select * from bookinformation "+command+" order by accession_no asc");
    // ps=con.prepareStatement("select * from bookinformation ");
     rs=ps.executeQuery();

     System.out.println("Query executed");
     int i=0;

     while(rs.next())
     i++;
     model.setRowCount(i);

     rs.first();
     rs.previous();

     i=0;
     while(rs.next())
     {
    
      model.setValueAt(rs.getString(9), i, 0);
      model.setValueAt(rs.getString(1), i, 1);
      model.setValueAt(rs.getString(2), i, 2);
      model.setValueAt(rs.getString(3), i, 3);
      model.setValueAt(publisher.get(rs.getInt(4)), i, 4);
      model.setValueAt(rs.getString(5), i, 5);
      model.setValueAt(rs.getString(6), i, 6);
      model.setValueAt(rs.getString(7), i, 7);
      model.setValueAt(rs.getString(8), i, 8);
      model.setValueAt(rs.getString(10), i, 9);
      model.setValueAt(rs.getString(11), i, 10);
      model.setValueAt(type.get(rs.getInt(12)), i, 11);
      model.setValueAt(category.get(rs.getInt(13)), i, 12);
      model.setValueAt(rs.getString(14), i, 13);
      model.setValueAt(rs.getString(15), i, 14);
      model.setValueAt(rs.getString(16), i, 15);
      model.setValueAt(rs.getString(17), i, 16);
      model.setValueAt(vendor.get(rs.getInt(18)), i, 17);
      model.setValueAt(rs.getString(21), i, 18);
      model.setValueAt(rs.getString(22), i, 19); 

      i++;
     }
    // con.close();
     }
     catch(Exception e)
     {
      System.err.println("Error in book search"+e);
     }
    }

