public void loadHistory()
{
    System.out.println("KKKKKKKKK");
    try
    {
   //  con= new com.duc.DatabaseConnection.DatabaseConnection().dataConnection();
     ps=con.prepareStatement("select studentid,name,booknumber,booktitle,date_format(issuedate,'%d %M,%Y'),date_format(duedate,'%d %M,%Y'),date_format(returndate,'%d %M,%Y'),fine,studentlibraryrecord.status from studentlibraryrecord,studententry where studentid=id and issuedate>=? and issuedate<=? ");
     ps.setString(1, cmbStartDate.getText());
     ps.setString(2,cmbEndDate.getText());
     rs=ps.executeQuery();
     System.out.println("LLLLLLLLLLLLLLLLL");
     int i=0;
     while(rs.next())
     i++;
     
     dm.setRowCount(i);

     rs.first();
     rs.previous();
     i=0;

     while(rs.next())
     {
         dm.setValueAt(rs.getString(1), i, 0);
         dm.setValueAt(rs.getString(2), i, 1);
         dm.setValueAt(rs.getString(3), i, 2);
         dm.setValueAt(rs.getString(4), i, 3);
         dm.setValueAt(rs.getString(5), i, 4);
         dm.setValueAt(rs.getString(6), i, 5);

         if(rs.getInt(9)==0)
         dm.setValueAt(rs.getString(7), i, 6);
         else
         dm.setValueAt("Not Returned", i, 6);
         
         dm.setValueAt(rs.getString(8), i, 7);

         i++;
     }


    }
    catch(Exception e)
    {
        e.printStackTrace();
    }
}

