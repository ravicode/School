public void saveData()
{
     try
    {

        // con= new com.duc.DatabaseConnection.DatabaseConnection().dataConnection();

         ps=con.prepareStatement("select * from bookisbnentry where isbnnumber=?");
         ps.setString(1, txtISBN.getText());
         rs=ps.executeQuery();
         if(!rs.next())
         {
             JOptionPane.showMessageDialog(null, "This ISBN number does not exists.","Message",JOptionPane.WARNING_MESSAGE);
             return;
         }

         ps=con.prepareStatement("select * from bookentry where booknumber=?");
         ps.setString(1, txtBookNumber.getText());
         rs=ps.executeQuery();
         if(rs.next())
         {
             JOptionPane.showMessageDialog(null, "This Book number  already exists. Press Update to modify details.","Message",JOptionPane.WARNING_MESSAGE);
             return;
         }


         ps = con.prepareStatement("insert into bookentry (isbnnumber,booknumber,vendor,authority,entrydate) values (?,?,?,?,(select current_date()))");
         ps.setString(1, txtISBN.getText());
         ps.setString(2, txtBookNumber.getText());
         ps.setString(3, cmbVendor.getSelectedItem().toString());
         ps.setString(4, cmbAuthority.getSelectedItem().toString());
         ps.executeUpdate();

         ps=con.prepareStatement("insert into bookstatus (booknumber,available,issuedate) values (?,1,(select current_date()))");
         ps.setString(1,txtBookNumber.getText());
         ps.executeUpdate();
         
         JOptionPane.showMessageDialog(null, "Data Saved Sucessfully.","Message",JOptionPane.PLAIN_MESSAGE);
        // con.close();
       }
       catch(Exception e)
       {
           System.err.println("Error is "+ e);
       }
    
}


