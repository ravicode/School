    public void loadSavedData()
    {
         try
    {

        // con= new com.duc.DatabaseConnection.DatabaseConnection().dataConnection();
         ps = con.prepareStatement("select isbnnumber,vendor,authority from bookentry where booknumber=?");
         ps.setString(1, txtBookNumber.getText());
         rs = ps.executeQuery();

         if(!rs.next())
         {
            resetData();
            return;
         }
         

         rs.previous();

            while (rs.next())
            {
              txtISBN.setText(rs.getString(1));
              cmbVendor.setSelectedItem(rs.getString(2));
              cmbAuthority.setSelectedItem(rs.getString(3));
              
            }

              //con.close();
         }
    catch(Exception e)
    {
        System.err.println("Error is"+e);
    }

    }

