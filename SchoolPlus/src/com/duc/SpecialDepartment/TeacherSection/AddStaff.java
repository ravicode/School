    public void fetchStaffData(String code)
    {
        try
    {
    //  con= new com.duc.DatabaseConnection.DatabaseConnection().dataConnection();
      ps=con.prepareStatement("Select * from staffentry where code=?");
      ps.setString(1,codeText.getText());
      rs=ps.executeQuery();

      if(!rs.next())
      {
        JOptionPane.showMessageDialog(this, "Invalid Staff Code", "Error Message", JOptionPane.ERROR_MESSAGE);
        resetWindow();
        //con.close();
        return;
      }      

      nameText.setText(rs.getString(2));
      fnameText.setText(rs.getString(3));
      phoneText.setText(rs.getString(4));
      emailText.setText(rs.getString(5));
      addressText.setText(rs.getString(6));

      if(rs.getString(7).equals("M"))
      male.setSelected(true);
      else
      female.setSelected(true);


      dob.setText(rs.getString(8));
      dob.repaint();

      degreeText.setText(rs.getString(9));
      cmbstafftype.setSelectedItem(rs.getString(10));
      cmbstaffmode.setSelectedItem(rs.getString(11));
designation.setSelectedItem(rs.getString(13));
      doj.setText(rs.getString(12));
      doj.repaint();

      //con.close();
      loadPicture();
      loadMoreInformation();
      }
        catch(Exception e)
        {
           System.err.println("Error is ..."+e);
        }
    }

