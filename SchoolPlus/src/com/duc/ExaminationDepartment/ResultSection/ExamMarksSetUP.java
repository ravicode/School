    public void saveExamMarks()
 {
     int i=0;
     try
      {
        ps=con.prepareStatement("delete from examsetup");
        ps.executeUpdate();
        
        for(i=0;i<=5;i++)
        {
        ps=con.prepareStatement("insert into examsetup (examname,exam_max_marks) values(?,?)");
        ps.setString(1,String.valueOf(dm.getValueAt(i, 0)));
        ps.setInt(2,Integer.parseInt(String.valueOf(dm.getValueAt(i, 1))));
        ps.executeUpdate();        
        }
        JOptionPane.showMessageDialog(null, "Saved Successfully", "Message", JOptionPane.INFORMATION_MESSAGE);
      } 
   catch(Exception e)
   {
    System.err.println("Error  "+e);
    e.printStackTrace();   
   }
 }

