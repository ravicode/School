public void saveStartNumber()
{
   // con=new com.duc.DatabaseConnection.DatabaseConnection().dataConnection();
    try
    {
ps=con.prepareStatement("select * from receiptnumber");
rs=ps.executeQuery();
if(!rs.next())
{
        ps=con.prepareStatement("insert into receiptnumber values(?,?,?,?,?,?,?,?,?,?)");
        ps.setInt(1, Integer.parseInt(receiptno.getText()));
        ps.setString(2,sid.getText());
        ps.setString(3,dat.getText().trim());
         ps.setString(4,mon.getText());
        ps.setDouble(5,Double.parseDouble(sur.getText()));
        ps.setDouble(6,Double.parseDouble(waiver.getText()));

        ps.setInt(7,sessionValue);
         ps.setDouble(8,Double.parseDouble(sur1.getText()));
        ps.setDouble(9,Double.parseDouble(waiver1.getText()));
        ps.setString(10,"ducc");
        ps.executeUpdate();
        jLabel7.setText("Student Fee Slip will Start from Slip no."+Integer.parseInt(receiptno.getText()));

    }
else
    jLabel7.setText("Receiptnumber already set");
    }
    catch(Exception e)
    {
        e.printStackTrace();
    }
}

