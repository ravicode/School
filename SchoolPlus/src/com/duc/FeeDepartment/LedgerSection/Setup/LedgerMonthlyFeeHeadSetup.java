public void oldLedgerFeeHead()
{

        // con= new com.duc.DatabaseConnection.DatabaseConnection().dataConnection();
       try
       {
           ps = con.prepareStatement("select * from monthlyfeeamounthead");

           rs = ps.executeQuery();
            int i = 0,j=0;
          while(rs.next())
            {
                j++;
            }
            rs.first();
            rs.previous();
            dm.setRowCount(j);
            while(rs.next())
            {
                dm.setValueAt(classmap.get(rs.getInt(3)), i, 0);
                dm.setValueAt(monthlyfeemap.get(rs.getInt(1)), i, 1);
                dm.setValueAt(rs.getInt(4), i, 3);
ps = con.prepareStatement("select * from ledgerheadsetup where ledgercode=?");
ps.setInt(1,rs.getInt(4));
rs1 = ps.executeQuery();
rs1.next();
 dm.setValueAt(rs1.getString(1), i, 2);
                i++;
            }
           // con.close();
            System.out.println("Database closed");

       }
       catch(Exception e)
       {
           System.err.println("Error"+e);
       }
    }

